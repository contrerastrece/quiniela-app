-- Trigger: crear perfil automáticamente al registrarse
DROP TRIGGER IF EXISTS after_auth_users_insert ON auth.users;
CREATE TRIGGER after_auth_users_insert
    AFTER INSERT ON auth.users
    FOR EACH ROW
    EXECUTE FUNCTION public.insert_user();

-- Trigger: calcular puntos cuando se actualiza un pronóstico a FINISHED
DROP TRIGGER IF EXISTS calculate_points_trigger ON public.tbl_predictions;
CREATE TRIGGER calculate_points_trigger
    AFTER UPDATE ON public.tbl_predictions
    FOR EACH ROW
    WHEN ((OLD.status_match IS DISTINCT FROM NEW.status_match)
          AND (NEW.status_match = 'FINISHED'::text))
    EXECUTE FUNCTION public.calculate_points_and_update_status();

