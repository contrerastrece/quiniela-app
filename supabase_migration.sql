-- insert_user: crea perfil en tbl_users cuando se registra un usuario
CREATE OR REPLACE FUNCTION public.insert_user()
RETURNS trigger
LANGUAGE plpgsql SECURITY DEFINER
AS $$
BEGIN
  insert into public.tbl_users (id, name, email, url_image)
  values (
    NEW.id,
    NEW.raw_user_meta_data->>'name',
    NEW.raw_user_meta_data->>'email',
    NEW.raw_user_meta_data->>'picture'
  );
  return NEW;
END;
$$;

-- calculate_points_and_update_status: calcula puntos según el sistema de puntaje
CREATE OR REPLACE FUNCTION public.calculate_points_and_update_status()
RETURNS trigger
LANGUAGE plpgsql
AS $$
DECLARE
    v_points INT := 0;
    pronostic_real TEXT;
    pronostic_user TEXT;
BEGIN
    -- Determinar el pronostico del usuario (L, V o E)
    IF NEW.score_home > NEW.score_visit THEN
        pronostic_user := 'L';
    ELSIF NEW.score_home < NEW.score_visit THEN
        pronostic_user := 'V';
    ELSE
        pronostic_user := 'E';
    END IF;

    -- Determinar el pronóstico real (L, V o E)
    IF NEW.result_home > NEW.result_visit THEN
        pronostic_real := 'L';
    ELSIF NEW.result_home < NEW.result_visit THEN
        pronostic_real := 'V';
    ELSE
        pronostic_real := 'E';
    END IF;

    -- Verificar si acertó cantidad de goles de local
    IF NEW.score_home = NEW.result_home THEN
        v_points := 1;
    END IF;

    -- Verificar si acertó cantidad de goles de visita
    IF NEW.score_visit = NEW.result_visit THEN
        v_points := v_points + 1;
    END IF;

    -- Verificar si acertó resultado final
    IF pronostic_user = pronostic_real THEN
        v_points := v_points + 1;
    END IF;

    -- Aplicar penalidad si no acertó nada
    IF v_points = 0 THEN
        v_points := -1;
    END IF;

    -- Aplicar bono si acertó todo (3 puntos base + 1 extra = 4)
    IF v_points = 3 THEN
        v_points := v_points + 1;
    END IF;

    -- Actualizar puntos y estado del partido en tbl_predictions
    UPDATE tbl_predictions
    SET points = v_points,
        status_match = 'FINISHED'
    WHERE id_user = NEW.id_user AND id_match = NEW.id_match;

    RETURN NEW;
END;
$$;

-- get_user_points: obtiene ranking de usuarios con puntos totales
CREATE OR REPLACE FUNCTION public.get_user_points()
RETURNS TABLE(id_user uuid, name text, url_image text, total_points integer)
LANGUAGE plpgsql
AS $$
BEGIN
    RETURN QUERY
    SELECT u.id as id_user, u.name, u.url_image,
           COALESCE(CAST(SUM(p.points) AS INTEGER), 0) AS total_points
    FROM tbl_users u
    INNER JOIN tbl_predictions p ON u.id = p.id_user
    GROUP BY u.id, u.name, u.url_image
    ORDER BY total_points DESC;
END;
$$;

